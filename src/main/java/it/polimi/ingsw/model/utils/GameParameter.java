package it.polimi.ingsw.model.utils;

/**
 * Possible parameters of an Action
 */
public enum GameParameter {
    /**
     * Number of players
     */
    NOF_PLAYERS,
    /**
     * Steps increment of mother nature
     */
    MOTHERNATURE_INCREMENT,
    /**
     * Order of a player
     */
    PLAYER_ORDER,
    /**
     * Index of an assistant card
     */
    ASSISTCARD_INDEX,
    /**
     * Player's id
     */
    PLAYER_ID,
    /**
     * Indexes of some students
     */
    THREE_STUDENTS_INDEXES,
    /**
     * Indees of some students
     */
    STUDENT_INDEXES,
    /**
     * Some places
     */
    THREE_STUDENTS_PLACES,
    /**
     * Index of an island
     */
    ISLAND_INDEX,
    /**
     * Some island indexes
     */
    ISLAND_INDEXES,
    /**
     * Index of a cloud
     */
    CLOUD_INDEX,
    /**
     * Desired number of students
     */
    DESIRED_NOFSTUDENTS,
    /**
     * Index of an entrance student
     */
    ENTRANCE_INDEX,
    /**
     * Index of a dining hall student
     */
    DINING_INDEX,
    /**
     * A color
     */
    COLOR,
    /**
     * Index of a character card
     */
    CHARACTER_CARD_INDEX,
    /**
     * Error message
     */
    ERROR_MSG,
    /**
     * Username
     */
    USERNAME,
    /**
     * Description
     */
    DESCRIPTION,
    /**
     * Extra steps performable by mother nature
     */
    MOTHERNATURE_EXTRASTEPS
}
