package it.polimi.ingsw.model.entities.cards;

/**
 * Possible behaviors of the character cards
 */
public enum Behaviors {
    /**
     * ability to lock islands
     */
    LOCK,
    /**
     * ability to move students
     */
    STUDENT,
    /**
     * ability to move mother nature
     */
    MOTHER_NATURE,
    /**
     * ability to set professors
     */
    PROFESSOR
}
